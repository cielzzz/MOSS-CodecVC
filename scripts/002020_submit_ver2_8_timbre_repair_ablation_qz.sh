      setup_batch3_recipe_base "batch3_r4_no_a4_recipebase"
      export TARGET_FRONT_CE_WEIGHT=1.0
      export TARGET_FRONT_CE_SECONDS=0.0
      ;;
    batch3_r5_fixed8s|batch3-r5-fixed8s|r5_fixed8s)
      setup_batch3_recipe_base "batch3_r5_fixed8s_recipebase"
      export REF_PROMPT_CODEC_PERMUTATION_MIN_SECONDS=8.0
      export REF_PROMPT_CODEC_PERMUTATION_MAX_SECONDS=8.0
      ;;
    A3|a3)
      suffix="a3_tref_prompt_slot_audiolike_frontce"
      export TIMBRE_SIDE_ONLY=1
