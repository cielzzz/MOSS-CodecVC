        exit 1
      fi
      ;;
    5)
      suffix="b2_no_source_prompt_placeholder"
      echo "[ver2.8-timbre-repair] ARM=5 is a placeholder for B2 source-prompt removal." >&2
      echo "[ver2.8-timbre-repair] Do not submit ARM=5 until a source-prompt-drop data path is implemented." >&2
      if [ "${ALLOW_ARM5_PLACEHOLDER:-0}" != "1" ]; then
