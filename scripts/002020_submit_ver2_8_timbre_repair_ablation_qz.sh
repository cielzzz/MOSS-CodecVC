    *)
      echo "ERROR: unsupported ARM=$arm; expected 0,1a,1b,1bprime,1bprime_a4,1c,1d,A3,1c_short,1d_long,1d_block,1d_block_varlen,1d_block_long,recipe_final,batch3_r1_no_infonce,batch3_r2_no_ref_suppression,batch3_r3_ordered_cref,batch3_r4_no_a4,batch3_r5_fixed8s,2,3,4,5,batch3_rollback or all" >&2
